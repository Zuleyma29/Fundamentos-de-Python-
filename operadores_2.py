# Operadores booleanos
# > >=
print(10>10)
print(10>=10)
# < <=
print(10<10)
print(10<=10)
# ==
print(10==10)
# !=
print(10!=10)


# Operadores logicos
# and (True if all True)
print(True and True)
print(True and False)
# or (True if all True)
print(True or True)
print(True or False)
# not (not True)
print(not True)
print(not False)

